package boskitakita.permadi.entity;

/**
 *
 * @author Hello Permadi
 */
public class ModelByHour {

    private String date;
    private String start;
    private String finish;
    private String grossDeliver;
    private String meterNumber;
    private String saleNumber;
    private String unitId;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getFinish() {
        return finish;
    }

    public void setFinish(String finish) {
        this.finish = finish;
    }

    public String getGrossDeliver() {
        return grossDeliver;
    }

    public void setGrossDeliver(String grossDeliver) {
        this.grossDeliver = grossDeliver;
    }

    public String getMeterNumber() {
        return meterNumber;
    }

    public void setMeterNumber(String meterNumber) {
        this.meterNumber = meterNumber;
    }

    public String getSaleNumber() {
        return saleNumber;
    }

    public void setSaleNumber(String saleNumber) {
        this.saleNumber = saleNumber;
    }

    public String getUnitId() {
        return unitId;
    }

    public void setUnitId(String unitId) {
        this.unitId = unitId;
    }

}
